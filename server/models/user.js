const mongoose = require('mongoose')
const schema = mongoose.Schema

const userSchema = new schema({
    firstname: {
        type: String,
        required: true,
        unique: true
    },
    lastname: {
        type: String,
        required: true,
        unique: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
})

const user = mongoose.model('user', userSchema)

module.exports = user
